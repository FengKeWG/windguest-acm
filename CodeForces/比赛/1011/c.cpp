void print(int x)
{
    if (x < 0)
    {
        putchar('-');
        x = -x;
    }
    if (x > 9)
        print(x / 10);
    putchar(x % 10 + '0');
}

inline int read()
{
    int x = 0, f = 1;
    char c = getchar();
    while (c < '0' || c > '9')
    {
        if (c == '-')
            f = -1;
        c = getchar();
    }
    while (c >= '0' && c <= '9')
    {
        x = (x << 1) + (x << 3) + (c ^ 48);
        c = getchar();
    }
    return x * f;
}

signed main()
{
    cin.tie(0)->sync_with_stdio(0);
    int T = read();
    while (T--)
    {
        int x = read(), y = read();
        if (x == y)
        {
            cout << -1 << endl;
            continue;
        }
        int k = 0;
        int mask = 1;
        while (mask <= max(x, y))
        {
            if ((x & mask) && (y & mask))
            {
                k |= mask;
            }
            mask <<= 1;
        }
        cout << k << endl;
    }
    return 0;
}