inline int read()
{
    int x = 0, f = 1;
    char c = getchar();
    while (c < '0' || c > '9')
    {
        if (c == '-')
            f = -1;
        c = getchar();
    }
    while (c >= '0' && c <= '9')
    {
        x = (x << 1) + (x << 3) + (c ^ 48);
        c = getchar();
    }
    return x * f;
}

signed main()
{
    cin.tie(0)->sync_with_stdio(0);
    double x = read(), y = read(), z = read(), n = read();
    double xx = x / n;
    for (int i = 1; i <= n; i++)
    {
        cout << fixed << setprecision(10) << (i - 1) * xx << " 0 0 " << i * xx << " " << y << " " << z << endl;
    }
    return 0;
}